@Injectable({
  providedIn: 'root'
})
export class TranslationService {
  private lang = signal<Language>('pt');

  currentLang = this.lang.asReadonly();

  constructor() {
    if (typeof localStorage !== 'undefined') {
      const storedLang = localStorage.getItem('clyro-lang') as Language;
      if (storedLang && TRANSLATIONS[storedLang]) {
        this.lang.set(storedLang);
      }
    }
  }

  setLanguage(language: Language) {
    this.lang.set(language);
    if (typeof localStorage !== 'undefined') {
      localStorage.setItem('clyro-lang', language);
    }
  }

  translate = computed(() => {
    const currentTranslations = TRANSLATIONS[this.lang()];
    const fallbackTranslations = TRANSLATIONS['en'];
    return (key: string, params?: Record<string, string | number>): string => {
      let translation = currentTranslations[key] || fallbackTranslations[key] || key;
      if (params) {
        Object.keys(params).forEach(paramKey => {
          translation = translation.replace(`{${paramKey}}`, String(params[paramKey]));
        });
      }
      return translation;
    };
  });
}